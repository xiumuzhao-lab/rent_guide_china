# ── 校验函数 ──────────────────────────────────────────────

def extract_district(location):
    """
    从 location 字段提取区名.

    Args:
        location: 如 "松江-泗泾-祥泽苑" 或 "东城区-安定门-XX"

    Returns:
        str: 区名 (如 "松江")，无法提取时返回空串
    """
    if not location:
        return ''
    parts = location.split('-')
    return parts[0].strip() if parts else ''


def validate_coords(lat, lng, location):
    """
    校验坐标是否在预期区的合理范围内.

    三层校验: 1) 城市边界 2) 区级 3) 子区域/板块级

    Args:
        lat: 纬度
        lng: 经度
        location: location 字段 (如 "松江-泗泾-祥泽苑")

    Returns:
        bool: True 表示坐标合理，False 表示坐标异常
    """
    # 第 1 层: 城市边界
    in_sh = (SH_BOUNDARY['lat_min'] <= lat <= SH_BOUNDARY['lat_max']
             and SH_BOUNDARY['lng_min'] <= lng <= SH_BOUNDARY['lng_max'])
    in_bj = (BJ_BOUNDARY['lat_min'] <= lat <= BJ_BOUNDARY['lat_max']
             and BJ_BOUNDARY['lng_min'] <= lng <= BJ_BOUNDARY['lng_max'])
    in_sz = (SZ_BOUNDARY['lat_min'] <= lat <= SZ_BOUNDARY['lat_max']
             and SZ_BOUNDARY['lng_min'] <= lng <= SZ_BOUNDARY['lng_max'])
    in_hz = (HZ_BOUNDARY['lat_min'] <= lat <= HZ_BOUNDARY['lat_max']
             and HZ_BOUNDARY['lng_min'] <= lng <= HZ_BOUNDARY['lng_max'])
    if not in_sh and not in_bj and not in_sz and not in_hz:
        logger.debug(f"坐标超出城市边界: ({lat}, {lng})")
        return False

    # 第 2 层: 区级校验
    district = extract_district(location)
    if district and district in DISTRICT_CENTERS:
        center_lat, center_lng = DISTRICT_CENTERS[district]
        dist = haversine(lat, lng, center_lat, center_lng)
        tolerance = DISTRICT_TOLERANCE_KM.get(district, 25)
        if dist > tolerance:
            logger.debug(
                f"坐标校验失败(区级): {location} -> ({lat}, {lng}) "
                f"距 {district} 中心 {dist:.0f}km (容忍 {tolerance}km)")
            return False

    # 第 3 层: 子区域级校验
    if location:
        parts = [p.strip() for p in location.split('-') if p.strip()]
        if len(parts) >= 2:
            sub_region = parts[1]
            center = SUB_REGION_CENTERS.get(sub_region)
            if not center:
                for name, coord in SUB_REGION_CENTERS.items():
                    if sub_region in name or name in sub_region:
                        center = coord
                        break
            if center:
                dist = haversine(lat, lng, center[0], center[1])
                if dist > SUB_REGION_TOLERANCE_KM:
                    logger.debug(
                        f"坐标校验失败(板块级): {location} -> "
                        f"({lat}, {lng}) 距 {sub_region} "
                        f"{dist:.0f}km (容忍 {SUB_REGION_TOLERANCE_KM}km)")
                    return False

    return True
